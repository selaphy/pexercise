# 二进制转10进制
print(int('100', 2))

# 十六进制转十进制
print(int('0x0a', 16))

# 十进制转十六进驻
print(hex(10))

# 十六进制转二进制
print(bin(0x0a))

f1 = 3.00
print(round(f1, 1))
